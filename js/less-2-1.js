// создаём массив
var animals = ['cat', 'dog', 'horse']
alert(animals)

// push() доб.эл в конец массиыва
animals.push('fish')
alert(animals)

// доб. эл в начало массива
animals.unshift('goose')
alert(animals)

// pop() УДАЛЯЕТ первый эл с конца массиыва
animals.pop()
alert(animals)

// shift() удаляет эл с начала массива
animals.shift()
alert(animals)